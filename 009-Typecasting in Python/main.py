a = "1"       #Here a is a string
# a = 1       #Here a is an int   
b = "2"       #Here b is a string
# b = 2       #Here b is a int
print(int(a) + int(b))        

# Implicit TypeCasting
c = 1.9      #Here c is taken as float 
d = 8        #Here d is taken as int

print(c + d)

print(type(c))
print(type(d))

#Typecasting-->The conversion of one data type into the other data type is known as type casting in python or type conversion in python.

#Two types of typecasting
# 1.Explicit Conversion (Explicit type casting in python)(done by user)
# 2.Implicit Conversion (Implicit type casting in python).(done by python interpreter)